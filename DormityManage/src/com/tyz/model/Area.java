package com.tyz.model;

public class Area {
	
	private int ar_id;
	private String ar_name;
	private int ar_uid;
	private int ar_sort;
	
	public int getAr_id() {
		return ar_id;
	}
	public void setAr_id(int ar_id) {
		this.ar_id = ar_id;
	}
	public String getAr_name() {
		return ar_name;
	}
	public void setAr_name(String ar_name) {
		this.ar_name = ar_name;
	}
	public int getAr_uid() {
		return ar_uid;
	}
	public void setAr_uid(int ar_uid) {
		this.ar_uid = ar_uid;
	}
	public int getAr_sort() {
		return ar_sort;
	}
	public void setAr_sort(int ar_sort) {
		this.ar_sort = ar_sort;
	}
	
	
	
}
